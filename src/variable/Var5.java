package variable;

public class Var5 {
    public static void main(String[] args){
//        int a;
//        int b;
//
//        int c,d;
        int a; // 변수 선언
        a = 10; //초기화
        System.out.println(a);

        int b = 10; // 변수 선언 및 초기화
        System.out.println(b);

        int c = 20, d = 30; // 타입이 같으면 동시에 작성 가능.
        System.out.println(c);
        System.out.println(d);
    }
}
